package EvdeCalisma1;

public class EvdeCalisma3 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        System.out.println("WHILE -----------------------");

        int j = 1;
        while (j <= 5) ;
        {
            System.out.println(j);
            j = j + 1;

        }
        System.out.println("do-while................");
        int m = 1;
        do {
            System.out.println(m);
            m++;
        } while (m <= 5);

    }


}



